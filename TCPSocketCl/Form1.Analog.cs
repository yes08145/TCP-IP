        //전류출력설정 버튼 클릭
        private void Btn_output_a_Click(object sender, EventArgs e)
        {
            sensorID = 1;
            if (metroLabel_outch0_a.BackColor == Color.FromArgb(0, 174, 219)) aout_ch = 0;
            else if (metroLabel_outch1_a.BackColor == Color.FromArgb(0, 174, 219)) aout_ch = 1;
            //else aout_ch = 404;
            else sensorID = 100;
            try
            {
                data = Convert.ToInt32(mAdata);
                foreach (SocketInfo usedSockInfo in socketInfo)
                {
                    if (usedSockInfo.index == dgv_constate.SelectedRows[0].Index)//Convert.ToInt32(listBox_quick.SelectedItem.ToString().Split(')')[0])-1)
                    {
                        if (usedSockInfo.conn)
                        {
                            StartThread(usedSockInfo, Send, "send");
                        }
                    }
                }
            }
            catch (NullReferenceException ex)
            {
                MessageBox.Show("설정 값을 보낼 서버를 선택해주십시오.");
            }
            catch (ArgumentOutOfRangeException ex)
            {
                MessageBox.Show("설정 값을 보낼 서버를 선택해주십시오.");
            }
            metroTabControl_AO_a.Focus();
        }
